public class Objective7Lab4 {
  public static void main(String[] args) {
    int counter = 0;
    int newCounter = 0;
    
    
    
    
    while(counter < 20) {
      //System.out.println(counter);
      counter = counter + 1; //this makes we count down
      newCounter += counter;
     
    }
    System.out.println(newCounter);
  }
}