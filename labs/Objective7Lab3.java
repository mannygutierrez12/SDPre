public class Objective7Lab3 {
  public static void main(String[] args) {
    int counter = 1;
    String isOdd;

    while(counter<=20){
      
      if(counter%2 == 0){
        isOdd = "is even";
      }else{
        isOdd = "is odd";
        

    
        
      }
        System.out.println(counter + " " + isOdd);
        counter = counter + 1;
      }
    }
}
